package com.twinkle.cloud.authserver.interceptor;

import com.twinkle.cloud.authserver.config.UserConfiguration;
import com.twinkle.cloud.authserver.util.user.JwtTokenUtil;
import com.twinkle.cloud.common.context.BaseContextHandler;
import com.twinkle.cloud.common.jwt.IJwtInfo;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.method.HandlerMethod;
import org.springframework.web.servlet.handler.HandlerInterceptorAdapter;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

