package com.twinkle.cloud.common.auth.interceptor;

import com.twinkle.cloud.common.auth.config.ServiceAuthConfig;
import com.twinkle.cloud.common.auth.jwt.ServiceAuthUtil;
import com.twinkle.cloud.common.auth.annotation.IgnoreClientToken;
import com.twinkle.cloud.common.auth.exception.ClientForbiddenException;
import com.twinkle.cloud.common.jwt.IJwtInfo;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.method.HandlerMethod;
import org.springframework.web.servlet.handler.HandlerInterceptorAdapter;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.util.List;

